package com.keerthana.selenium;

public class Calculator {

	int add(int a, int b) {
		return a + b;
	}

	float add(float a, float b) {
		return a + b;
	}

	int subtraction(int a, int b) {
		return a - b;
	}

	int multiplication(int a, int b) {
		return a * b;
	}

	int division(int a, int b) {
		return a / b;
	}

}
